DROP TABLE IF EXISTS Media CASCADE;
DROP TABLE IF EXISTS Game_Genre CASCADE;
DROP TABLE IF EXISTS Genre CASCADE;
DROP TABLE IF EXISTS Game CASCADE;
DROP TABLE IF EXISTS Users CASCADE;

CREATE TABLE Users (
    user_id SERIAL PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    country VARCHAR(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Game (
    game_id SERIAL PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    description TEXT,
    release_date DATE,
    base_price NUMERIC(10,2) NOT NULL CHECK (base_price >= 0),
    is_free BOOLEAN DEFAULT FALSE,
    developer VARCHAR(100),
    publisher VARCHAR(100),
    age_rating VARCHAR(20)
);

CREATE TABLE Genre (
    genre_id SERIAL PRIMARY KEY,
    genre_name VARCHAR(50) UNIQUE NOT NULL
);

CREATE TABLE Game_Genre (
    game_id INT REFERENCES Game(game_id) ON DELETE CASCADE,
    genre_id INT REFERENCES Genre(genre_id) ON DELETE CASCADE,
    PRIMARY KEY (game_id, genre_id)
);

CREATE TABLE Media (
    media_id SERIAL PRIMARY KEY,
    game_id INT NOT NULL REFERENCES Game(game_id) ON DELETE CASCADE,
    media_type VARCHAR(20) CHECK (media_type IN ('screenshot', 'trailer')),
    media_url TEXT NOT NULL
);

CREATE TABLE Wishlist_Item (
    user_id INT REFERENCES Users(user_id) ON DELETE CASCADE,
    game_id INT REFERENCES Game(game_id) ON DELETE CASCADE,
    added_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, game_id)
);

CREATE TABLE Cart (
    cart_id SERIAL PRIMARY KEY,
    user_id INT UNIQUE NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Cart_Item (
    cart_id INT REFERENCES Cart(cart_id) ON DELETE CASCADE,
    game_id INT REFERENCES Game(game_id) ON DELETE CASCADE,
    added_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (cart_id, game_id)
);

CREATE TABLE Orders (
    order_id SERIAL PRIMARY KEY,
    user_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    order_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    total_amount NUMERIC(10,2) NOT NULL CHECK (total_amount >= 0)
);

CREATE TABLE Order_Item (
    order_id INT REFERENCES Orders(order_id) ON DELETE CASCADE,
    game_id INT REFERENCES Game(game_id) ON DELETE CASCADE,
    price_paid NUMERIC(10,2) NOT NULL CHECK (price_paid >= 0),
    PRIMARY KEY (order_id, game_id)
);

CREATE TABLE Ownership (
    user_id INT REFERENCES Users(user_id) ON DELETE CASCADE,
    game_id INT REFERENCES Game(game_id) ON DELETE CASCADE,
    acquired_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, game_id)
);

CREATE TABLE Review (
    review_id SERIAL PRIMARY KEY,
    user_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    game_id INT NOT NULL REFERENCES Game(game_id) ON DELETE CASCADE,
    rating INT CHECK (rating BETWEEN 1 AND 5),
    review_text TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (user_id, game_id)
);

CREATE TABLE Friend_Request (
    request_id SERIAL PRIMARY KEY,
    sender_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    receiver_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    status VARCHAR(20) CHECK (status IN ('pending', 'accepted', 'rejected')),
    requested_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CHECK (sender_id <> receiver_id)
);

CREATE TABLE Friendship (
    user_id INT REFERENCES Users(user_id) ON DELETE CASCADE,
    friend_id INT REFERENCES Users(user_id) ON DELETE CASCADE,
    friends_since TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, friend_id),
    CHECK (user_id <> friend_id)
);

CREATE TABLE Message (
    message_id SERIAL PRIMARY KEY,
    sender_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    receiver_id INT NOT NULL REFERENCES Users(user_id) ON DELETE CASCADE,
    sent_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    message_text TEXT NOT NULL,
    CHECK (sender_id <> receiver_id)
);


